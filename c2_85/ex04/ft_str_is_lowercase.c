int	ft_str_is_lowercase(char *str)
{
	char	*ptr;

	ptr = str;
	while (*ptr != 0)
	{
		if (*ptr < 'a' || *ptr > 'z')
		{
			return (0);
		}
		ptr++;
	}
	return (1);
}
