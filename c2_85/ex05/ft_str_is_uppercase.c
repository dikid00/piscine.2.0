int	ft_str_is_uppercase(char *str)
{
	char	*ptr;

	ptr = str;
	while (*ptr != 0)
	{
		if (*ptr < 'A' || *ptr > 'Z')
		{
			return (0);
		}
		ptr++;
	}
	return (1);
}
