int	ft_str_is_alpha(char *str)
{
	char	*ptr;

	ptr = str;
	while (*ptr != 0)
	{
		if (*ptr < 'A')
		{
			return (0);
		}
		if ((*ptr > 'Z' && *ptr < 'a') || (*ptr > 'z'))
		{
			return (0);
		}
		ptr++;
	}
	return (1);
}
