int	ft_str_is_numeric(char *str)
{
	char	*ptr;

	ptr = str;
	while (*ptr != 0)
	{
		if (*ptr < '0' || *ptr > '9')
		{
			return (0);
		}
		ptr++;
	}
	return (1);
}
